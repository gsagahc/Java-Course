package com.simple.helloworld;

public class HelloWorld {
	private Long id;
	private String mensagem;
	
	
	public HelloWorld(Long id, String mensagem) {
		this.id = id;
		this.mensagem = mensagem;
	}
	public Long getId() {
		return id;
	}
	public String getMensagem() {
		return mensagem;
	}
	
	  

}
